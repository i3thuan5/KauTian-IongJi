

def 全部標點():
    return {
        ('。', '.'), ('，', ','), ('、', ','), ('；', ';'),
        ('：', ':'), ('？', '?'), ('！', '!'),
        # 引號
        ('「', '"'), ('」', '"'), ('「', '“'), ('」', '”'),
        ('『', "'"), ('』', "'"), ('『', "‘"), ('』', "’"),
        # 書名號
        ('《', '"'), ('》', '"'), ('《', '“'), ('》', '”'),
        ('〈', '"'), ('〉', '"'), ('〈', '“'), ('〉', '”'),
        # 夾注號甲式
        ('（', '('), ('）', ')'),
        # 破折號、夾注號乙式
        ('——', '——'),
        # 刪節號
        ('……', '...'),
        # 連接號
        ('～', '~'), ('—', '-'),
        # 間隔號
        ('．', '．'),
    }


def 提全部標點():
    return 全部標點()
